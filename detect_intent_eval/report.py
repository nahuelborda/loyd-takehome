"""Renders scorer + taxonomy output as an interpretable Markdown report."""

from __future__ import annotations

import datetime as _dt

from .dataset import LABELS, Email
from .predictor import Prediction
from .taxonomy import FAILURE_MODES, is_acceptable, tag_failures


def _table(headers, rows) -> str:
    out = [
        "| " + " | ".join(map(str, headers)) + " |",
        "| " + " | ".join("---" for _ in headers) + " |",
    ]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out)


def _f(x, nd: int = 3) -> str:
    if x is None:
        return "—"
    if isinstance(x, float):
        return f"{x:.{nd}f}"
    return str(x)


def build_report(
    emails: list[Email],
    preds: list[Prediction],
    model: str,
    cm: dict,
    none_res: dict,
    cal: dict,
    amb: dict,
    route: dict,
) -> str:
    pred_by_id = {p.email_id: p for p in preds}
    accept_acc = sum(
        is_acceptable(e, pred_by_id[e.id].label) for e in emails
    ) / len(emails)
    L: list[str] = []
    w = L.append

    w("# Eval Report — `_detect_intent`")
    w("")
    w(f"**Model:** `{model}` · **Emails:** {len(emails)} · "
      f"**Generated:** {_dt.date.today().isoformat()}")
    w("")
    w("Auto-generated by `run_eval.py`. The written interpretation is in "
      "[`interpretation.md`](interpretation.md).")
    w("")

    w("## Headline numbers")
    w("")
    w(_table(
        ["Metric", "Value", "Note"],
        [
            ["Exact accuracy — clean rows",
             f"{_f(cm['clean_accuracy'])} (n={cm['n_clean']})",
             "cleanest signal — single correct label"],
            ["Macro-F1 — all 25", _f(cm["macro_f1"]),
             "rare classes weighted equally"],
            ["Accuracy — all 25 (strict exact-match)", _f(cm["accuracy"]),
             "every row scored against its gold label"],
            ["Accuracy — all 25 (acceptable-set)", _f(accept_acc),
             "ambiguous rows credited for gold OR alt_label — matches the per-email ✓ column"],
            ["ECE", _f(cal["ece"]), "calibration error — unstable at n=25"],
            [">0.95 auto-send precision",
             f"{_f(cal['autosend_band']['precision'])} "
             f"(n={cal['autosend_band']['n']})",
             "errors here ship unreviewed"],
        ],
    ))
    w("")

    w("## Confusion matrix (rows = gold, columns = predicted)")
    w("")
    w(_table(
        ["gold ↓ / pred →"] + LABELS,
        [[lab] + cm["confusion_matrix"][i] for i, lab in enumerate(LABELS)],
    ))
    w("")

    w("## Per-class metrics")
    w("")
    w(_table(
        ["Label", "Precision", "Recall", "F1", "Support"],
        [[c["label"], _f(c["precision"]), _f(c["recall"]), _f(c["f1"]), c["support"]]
         for c in cm["per_class"]],
    ))
    w("")
    w("> Classes with support < 5 are high-variance — read directionally only.")
    w("")

    w("## `none` / out-of-scope detection")
    w("")
    w(_table(
        ["Metric", "Value"],
        [
            ["`none` precision", _f(none_res["precision"])],
            ["`none` recall", _f(none_res["recall"])],
            ["real request dropped to `none` (fp — lowers precision)", none_res["fp"]],
            ["junk acted on as a request (fn — lowers recall)", none_res["fn"]],
        ],
    ))
    w("")
    w("By how Loyd was addressed:")
    w("")
    w(_table(
        ["loyd_addressed", "n", "gold=none", "→ none ✓", "→ intent ✗"],
        [[k, v["n"], v["gold_none"], v["pred_none_correct"], v["false_intent"]]
         for k, v in sorted(none_res["by_addressing"].items())],
    ))
    w("")

    w("## Calibration")
    w("")
    w(_table(
        ["Confidence bin", "n", "mean conf", "accuracy"],
        [[f"{b['range'][0]:.2f}–{min(b['range'][1], 1.0):.2f}", b["n"],
          _f(b["mean_conf"]), _f(b["accuracy"])] for b in cal["bins"]],
    ))
    w("")
    asb = cal["autosend_band"]
    w(f"**Auto-send band (confidence > 0.95):** {asb['n']} emails "
      f"({asb['share'] * 100:.0f}% of traffic), precision {_f(asb['precision'])}. "
      "Every error in this band reaches the other party with no human review.")
    w("")

    w("## HITL routing — operating curve")
    w("")
    w(_table(
        ["Threshold", "HITL volume", "auto n", "auto precision",
         "escaped errors", "escaped rate"],
        [[f"{r['threshold']:.2f}", f"{r['hitl_volume'] * 100:.0f}%", r["auto_n"],
          _f(r["autosend_precision"]), r["escaped_errors"],
          _f(r["escaped_error_rate"])] for r in route["curve"]],
    ))
    w("")
    w("> Higher threshold → more goes to HITL (cost) but fewer errors escape (risk). "
      "Loyd's live auto-send bypass sits at 0.95. The threshold column counts emails with "
      "confidence ≥ threshold; the headline >0.95 band uses strict > 0.95, so counts can "
      "differ by one at 0.95.")
    w("")

    w("## Ambiguity & uncertainty handling")
    w("")
    mc = amb["mean_confidence"]
    w(_table(
        ["label_fit tier", "mean confidence"],
        [["clean", _f(mc["clean"])], ["ambiguous", _f(mc["ambiguous"])],
         ["misfit", _f(mc["misfit"])]],
    ))
    w("")
    w(f"- **Confidence gap (clean − ambiguous): {_f(amb['confidence_gap_clean_minus_ambiguous'])}** "
      "— positive means the model is appropriately less sure on ambiguous inputs.")
    w(f"- Ambiguous acceptable-set hit rate: **{_f(amb['ambiguous_acceptable_set_rate'])}** "
      f"(n={amb['n_ambiguous']}) — prediction fell in {{gold ∪ alt_label}}.")
    w(f"- Misfit least-wrong matches: **{amb['misfit_least_wrong_matches']}/{amb['n_misfit']}**; "
      f"misfit emails answered with confidence > 0.95: **{amb['misfit_high_confidence_count']}** "
      "— these would auto-send despite fitting no label.")
    w("")

    w("## Failure-mode taxonomy")
    w("")
    counts: dict[str, list[str]] = {code: [] for code in FAILURE_MODES}
    for e in emails:
        for code in tag_failures(e, pred_by_id[e.id]):
            counts[code].append(e.id)
    w(_table(
        ["Code", "Failure mode", "n", "Emails"],
        [[code, FAILURE_MODES[code][0], len(ids), ", ".join(ids) or "—"]
         for code, ids in counts.items()],
    ))
    w("")

    w("## Per-email results")
    w("")
    rows = []
    for e in emails:
        p = pred_by_id[e.id]
        rows.append([
            e.id, e.gold_label, p.label, f"{p.confidence:.2f}",
            "✓" if is_acceptable(e, p.label) else "✗",
            e.label_fit, ", ".join(tag_failures(e, p)) or "—",
        ])
    w(_table(["ID", "gold", "pred", "conf", "ok", "fit", "failure tags"], rows))
    w("")
    return "\n".join(L)
